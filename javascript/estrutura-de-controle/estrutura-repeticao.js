let numero = 5;

for (let i = 1; i <= 10; i++) {
  console.log(numero + ' x ' + i + ' = ' + numero * i);
}

//while loop
let i = 0;
while (i <= 5){
  console.log(i); // imprime os números de 0 a 5
  i++;
}

//do while loop
let j = 0;
do {
  console.log(j); // imprime os números de 0 a 5
  j++;
}while (j <= 5);